/**
 * Write a program that displays the area and perimeter of a rectangle with the
 * width of 4.5 and height of 7.9 using the following formula: 
 * area = width * height
 * 
 * @author Leon N
 *
 */
public class Ex1_09 {
	public static void main(String[] args) {
		System.out.println("Perimeter of the rectangle with width of 4.5 and height of 7.9 is " +
				2 * (4.5 + 7.9));
		System.out.println("Area of the rectangle with width of 4.5 and height of 7.9 is " +
				(4.5 * 7.9));
	}
}
